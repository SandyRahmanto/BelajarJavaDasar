public class Main {

    public static void main(String[] args) {
        
        //Operator assigment
        
        //Assignment operator jumalh
        int a = 1;
        a += 10; // sama dengan a = a + 10;
        System.out.println("niali a = " + a);
        
        //Assignment operator pengurangan
        int b = 10;
        b -= 7; // sama dengan b = b - 10;
        System.out.println("niali b = " + b);
        
        //Assignment operator perkalian
        int c = 10;
        c *= 7; // sama dengan c = c * 10;
        System.out.println("niali c = " + c);
        
        //Assignment operator pembagian
        int d = 10;
        d /= 5; // sama dengan d = d / 10;
        System.out.println("niali d = " + d);
        
        //Assignment operator modulus
        int e = 10;
        e %= 6; // sama dengan e = e % 10;
        System.out.println("niali e = " + e);
        
    }
    
}
